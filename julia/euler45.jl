include("Euler.jl")

pent(n) = floor(Int, n*(3*n-1) / 2)
tri(n) = floor(Int, n*(n+1) / 2)
hex(n) = n*(2*n-1)

# 0.00013708s
function euler45a()
    t = 285+1
    tt = tri(t)
    p = 165
    pp = pent(p)
    h = 143
    hh = hex(h)
    while tt != pp || pp != hh
        t += 1
        tt = tri(t)
        if tt > pp p+=1; pp = pent(p) end
        if pp > hh h+=1; hh = hex(h) end
        if tt > hh h+=1; hh = hex(h) end
    end

    return tt
end

run_euler(euler45a)
