'use strict';

var {fib,sum,sum2,range,last,butLast,timing2} = require('./js_utils.js');

// Imperative: 0ms
var euler2a = function () {
    var limit = 4000000;
    var i = 1;
    var f = fib(i);
    var s = 0;
    while (f <= limit) {
        if (f % 2 == 0) {
            s += f;
        }
        i++;
        f = fib(i);
    }
    return(s);
}

// Mostly functional: 0ms
var euler2b = function() {
    var limit = 4000000;
    return sum(
        range(40)
            .map(i=>fib(i))
            .filter(i=>i<=limit && i % 2 === 0)
    );    
}

var euler2c = function() {
    var limit = 4000000;
    var a = [1,1];
    while (last(a) <= limit) {
        a.push(last(a)+butLast(a));
    }
    // return sum(a.filter(i=>i % 2 === 0));
    return a.filter(i=>i % 2 === 0).sum2();
}

var euler2d = function() {
    var f = 0;
    var s = 0;
    var i = 1;
    while (f <= 4000000) {
        s += (f = fib(i++)) % 2 == 0 ? f : 0;
    }
    return s;
}

timing2(euler2a);
